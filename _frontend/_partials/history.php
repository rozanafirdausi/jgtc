						The initial idea of Jazz Goes To Campus (JGTC) was founded by Candra Darusman in the 1976, who at that time was still a student at FEUI. Incidentally, jazz music was in demand by the public and students. Originally, JGTC was first held at the UI Salemba Campus MIPA Park, Jakarta, with makeshift facilities. Candra Darusman's goal at that time was very simple, "Bringing Jazz to Campus", especially because Jazz was an expensive music at the time, which was only available in luxury hotels and cafes. But now, JGTC has developed into a series of events for one month with the highlight JGTC Festival held in the campus area of FEUI Depok—along with the move of the lecture building—on four stages and supported by more than various performers, both local and international.
